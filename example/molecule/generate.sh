    sed -i "2s/.*/${MOL%.*}\/$METHOD\/$BASIS\/$ENERGY/" "$MOL"
    while read -r LINE; do
        echo "$LINE" | awk 'NF==4 {printf("%2s % 3.8f % 3.8f % 3.8f\n", $1, $2, $3, $4)}' >> "$MOL.tmp"
        echo "$LINE" | awk 'NF==1 {printf("%s\n", $1)}' >> "$MOL.tmp"
